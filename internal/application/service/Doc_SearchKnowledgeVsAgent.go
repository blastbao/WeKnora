package service

// AgentQA
//
//	AI 不仅依赖预设知识，而是能够根据当前的情况，自主决策、调用工具、搜索网络、甚至反思错误，最终组合出答案。
//
//	AgentQA 具备以下核心特征：
//	- 工具调用能力 (Tool Use)：它不仅仅查数据库，还可以调用外部工具（如代码解释器、日历、API 等）。
//	- 网络搜索 (Web Search)：如果配置开启，它能实时搜索互联网获取最新信息（这是纯检索做不到的）。
//	- MCP 服务集成：代码中提到了 MCPSelectionMode 和 MCPServices，这意味着它可以连接外部的数据和功能服务。
//	- 自主决策 (Orchestration)：它像一个导演，决定是先查知识库，还是先去搜网页，还是直接调用计算器。

// AgentQA 与 KnowledgeQA
//
// 1. 架构定位不同
//	KnowledgeQA 是传统的检索增强生成（RAG）模式。
//	它的核心思路是先检索相关知识，然后将检索结果塞给大模型生成答案。
//	整个流程是固定的管道结构：检索 → 重排序 → 合并 → 过滤 → 生成摘要。
//	模型在这里的角色主要是"总结者"，把找到的文档内容组织成通顺的回答。
//
//	AgentQA 是基于智能体（Agent）的自主决策模式。
//	它把大模型当作"决策者"，能够自主判断是否需要检索、调用什么工具、如何分解任务。
//	检索只是 Agent 可调用的工具之一，除此之外还可以调用网络搜索、MCP 服务、代码执行器等。
//	流程不是固定的，而是由 Agent 根据问题动态规划。
//
//
// 2. 配置来源与灵活性
//	KnowledgeQA 的配置来自租户级别的默认设置。
//	所有用户共享同一套检索参数：用哪个模型、检索几个知识库、温度多少，都是管理员统一配置好的。
//	用户只能被动使用，无法针对单次查询调整行为。
//
//	AgentQA 的配置来自自定义 Agent（CustomAgent）。
//	每个 Agent 有独立的完整配置：模型选择、工具权限、系统提示、知识库范围、是否启用反思、MCP 服务列表等。
//	用户可以创建多个 Agent，有的专门做数据分析（启用代码工具），有的专门做文献综述（绑定学术知识库），有的纯聊天（不绑定任何知识库）。
//	这种灵活性让同一个系统可以服务完全不同的使用场景。
//
//  3. 知识库选择策略
//	KnowledgeQA 的知识库范围是固定的。
//	启动查询时就确定了在哪些知识库里搜，整个会话期间不变。如果用户想换知识库，只能开新会话。
//
//	AgentQA 支持动态知识库选择。
//	用户可以在提问时用 @ 语法临时指定知识库，覆盖 Agent 的默认配置。
//	更关键的是 RetrieveKBOnlyWhenMentioned 选项：开启后，Agent 平时不检索知识库，只有用户明确 @ 了才检索。
//	这实现了"纯 Agent 模式"和"RAG 模式"的无缝切换。
//	Agent 也可以自主决定要不要检索——比如用户问"今天天气如何"，Agent 判断不需要查知识库，直接调用天气工具或网络搜索。
//
//
// 4. 对话历史管理
//	KnowledgeQA 的历史管理简单粗暴。
//	配置里设一个 HistoryTurns（比如 5 轮），超过就丢弃最早的消息。
//	这会导致长会话中早期上下文丢失，且无法压缩或总结历史。
//
//	AgentQA 使用专门的 ContextManager 管理历史。
//	它支持智能压缩策略：当 token 超过阈值时，自动总结早期对话为摘要，保留关键信息的同时控制长度。
//	还支持多轮对话开关（MultiTurnEnabled），关闭后每次查询都是独立的，适合不需要上下文的场景。
//	Agent 模式下，历史管理是独立的子系统，不是简单的截断逻辑。
//
// 5. 工具调用能力
//	这是两者最本质的区别。
//
//	KnowledgeQA 没有工具概念，它的能力边界就是"检索 + 生成"。
//	如果问题需要计算、查实时信息、操作外部系统，它无法完成。
//
//	AgentQA 内置工具系统，默认工具包括：
//	 - 知识检索：搜索绑定的知识库
//	 - 网络搜索：获取实时信息（配置 WebSearchEnabled 开启）
//	 - MCP 工具：调用外部 MCP 服务（如代码执行、数据库查询、API 调用）
//	Agent 可以组合使用多个工具，比如用户问"对比我们公司和竞品的 Q3 财报"，
//	Agent 可能：先用网络搜索找竞品财报 → 用知识检索查本公司内部数据 → 用代码工具计算增长率 → 最后生成对比分析。
//	这种多步骤、跨工具的推理是 KnowledgeQA 无法实现的。
//
//
// 6. 执行流程与输出方式
//	KnowledgeQA 是同步阻塞执行。
//	调用函数后，等待整个管道跑完（检索、重排、生成），最后返回完整的答案字符串。
//	前端需要等待，用户体验是"提交 → 转圈 → 出结果"。
//
//	AgentQA 是异步流式执行。
//	函数立即返回，真正的执行在后台进行。
//	执行过程中，Agent 通过 EventBus 实时发送事件：开始思考、调用工具、工具返回、生成内容、完成。
//	前端可以实时展示"正在搜索网络"、"正在计算数据"等中间状态，甚至展示思考过程（Thinking 选项开启时）。
//	这种透明性让用户感知到 Agent 的工作过程，而不是黑盒等待。
//
// 7. 反思与迭代能力
//	KnowledgeQA 没有反思机制，检索一次、生成一次，结束。
//
//	AgentQA 支持迭代反思（ReflectionEnabled）。
//	Agent 生成初稿后，可以自我检查：信息是否完整？逻辑是否有漏洞？是否需要补充检索？
//	然后进入下一轮迭代，直到满足停止条件（答案质量达标或达到 MaxIterations 上限）。
//	这种自我修正能力显著提高了复杂任务的完成质量。
//
// 8. 适用场景对比
//	KnowledgeQA 适合简单、明确的问答场景。
//	用户问题答案明确存在于知识库中，只需要找到相关段落并总结。
//	比如"员工请假流程是什么"、"产品 A 的技术参数有哪些"。
//	特点是：答案在文档里、不需要外部信息、不需要计算、单轮即可解决。
//
//	AgentQA 适合复杂、开放的任务。
//	问题可能需要多步推理、实时信息、外部工具配合，比如
//	 - "分析这份销售数据的趋势，并与上月对比，生成可视化图表"、
//	 - "帮我查一下这个 bug 的解决方案，如果知识库里找不到，就去 StackOverflow 搜，然后总结成中文"。
//	特点是：任务边界模糊、需要自主规划、可能涉及多轮工具调用、需要实时数据。
//
// 9. 错误处理与容错
//	KnowledgeQA 的错误处理是"硬失败"。
//	检索失败或模型调用失败，直接返回错误，用户得不到任何答案。
//
//	AgentQA 的错误处理更灵活。
//	单个工具调用失败（比如网络搜索超时），Agent 可以捕获错误、调整策略、尝试其他工具或告知用户部分结果。
//	错误通过 EventBus 流式发送，前端可以展示"网络搜索失败，仅基于知识库回答"这样的降级提示，而不是完全失败。
//
// 10. 系统提示与角色定义
//	KnowledgeQA 使用系统默认提示，所有查询共享同一套"你是一个 helpful 的助手"式提示。
//
//	AgentQA 支持自定义系统提示（SystemPrompt）。
//	可以为不同 Agent 设定完全不同的角色：
//	 - 有的是严谨的分析师（"你是一名数据分析师，所有结论必须有数据支撑"），
//	 - 有的是创意写手（"你是一名文案策划，风格活泼有感染力"），
//	 - 有的是技术支持（"你是一名技术支持工程师，优先参考知识库内容，不确定时建议提交工单"）。
//	角色定义深度影响 Agent 的行为模式。
